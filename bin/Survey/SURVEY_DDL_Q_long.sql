
CREATE TABLE ANSWER
(
  ANSWER    VARCHAR(50) NOT NULL COMMENT '응답',
  ANSWER_ID VARCHAR(50) NOT NULL COMMENT '응답_ID',
  PRIMARY KEY (ANSWER_ID)
) COMMENT '응답';

CREATE TABLE RESULTS
(
  USER_ID    VARCHAR(50) NOT NULL COMMENT '설문자_ID',
  SURVEY_ID  VARCHAR(50) NOT NULL COMMENT '질문_ID',
  ANSWER_ID  VARCHAR(50) NOT NULL COMMENT '응답_ID',
  RESULTS_ID VARCHAR(50) NOT NULL COMMENT '결과_ID',
  PRIMARY KEY (RESULTS_ID)
) COMMENT '결과';

CREATE TABLE SURVEY
(
  SURVEY    VARCHAR(500) NOT NULL COMMENT '질문',
  SURVEY_ID VARCHAR(50)  NOT NULL COMMENT '질문_ID',
  PRIMARY KEY (SURVEY_ID)
) COMMENT '질문';

CREATE TABLE USER
(
  USER    VARCHAR(50) NOT NULL COMMENT '설문자',
  USER_ID VARCHAR(50) NOT NULL COMMENT '설문자_ID',
  PRIMARY KEY (USER_ID)
) COMMENT '설문자';

ALTER TABLE RESULTS
  ADD CONSTRAINT FK_USER_TO_RESULTS
    FOREIGN KEY (USER_ID)
    REFERENCES USER (USER_ID);

ALTER TABLE RESULTS
  ADD CONSTRAINT FK_SURVEY_TO_RESULTS
    FOREIGN KEY (SURVEY_ID)
    REFERENCES SURVEY (SURVEY_ID);

ALTER TABLE RESULTS
  ADD CONSTRAINT FK_ANSWER_TO_RESULTS
    FOREIGN KEY (ANSWER_ID)
    REFERENCES ANSWER (ANSWER_ID);
